export interface ShapFeature {
  name: string;
  contribution: number; // signed: negative pulls toward churn / risk, positive pulls toward stay / upgrade
}

export interface ModelMeta {
  name: string;             // e.g. "anomaly_detect_v3.1"
  family: string;           // e.g. "Snowflake ML · Forecasting"
  version: string;          // e.g. "v3.1"
  lastTrained: string;      // ISO date
  auc: number;              // 0..1
  drift: 'OK' | 'Warning' | 'Drifting';
  confidence: number;       // 0..1
  scoreLabel: string;       // e.g. "Anomaly score"
  scoreValue: string;       // e.g. "0.94"
  ci: string;               // e.g. "0.91–0.97"
  topFeatures: ShapFeature[];
  trainedRows: string;      // human-readable, e.g. "12.4M cell-KPI rows"
  goldTable: string;        // primary feature/output table
}

